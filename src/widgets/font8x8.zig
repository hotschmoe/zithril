// Tests
const std = @import("std");

test "font8x8: get returns bitmap for supported characters" {
    const a_bitmap = Font8x8.get('A');
    try std.testing.expect(a_bitmap != null);
    try std.testing.expectEqual(@as(u8, 0b00011000), a_bitmap.?[0]);
}

test "font8x8: get returns null for unsupported characters" {
    const result = Font8x8.get(0x1234);
    try std.testing.expect(result == null);
}

test "font8x8: all printable ASCII characters are supported" {
    var supported: usize = 0;
    for (32..127) |c| {
        if (Font8x8.get(@intCast(c)) != null) {
            supported += 1;
        }
    }
    // Should have at least space, a-z, A-Z, 0-9, basic punctuation
    try std.testing.expect(supported >= 80);
}

test "font8x8: space is all zeros" {
    const space = Font8x8.get(' ');
    try std.testing.expect(space != null);
    for (space.?) |row| {
        try std.testing.expectEqual(@as(u8, 0), row);
    }
}
